package org.example;

public class Circle {
    public int x, y, r;
    Circle(int x, int y, int r)  {
        this.x = x;
        this.y = y;
        this.r = r;
    }
    public int getX()   {
        return this.x;
    }
    public int getY()   {
        return this.y;
    }
    public int getR()   {
        return this.r;
    }
}
